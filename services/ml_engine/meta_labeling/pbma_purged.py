def apply_hmm_hard_gate(
    p_bma:          pd.Series,
    hmm_prob_bull:  pd.Series | np.ndarray,
    threshold:      float = HMM_GATE_THRESHOLD,
) -> pd.Series:
    """Zera p_bma onde hmm_prob_bull < threshold (bear market).

    v10.10.6 FIX:
      - NaN hmm ÃƒÂ¢Ã¢â‚¬Â Ã¢â‚¬â„¢ fillna(0.0) ÃƒÂ¢Ã¢â‚¬Â Ã¢â‚¬â„¢ treated as bear (conservador para long-only)
      - Usa reindex + pandas boolean masking (nÃƒÆ’Ã‚Â£o iloc[numpy_bool])
      - Se hmm_prob_bull ÃƒÆ’Ã‚Â© np.ndarray, cria Series com p_bma.index
    """
    if isinstance(hmm_prob_bull, pd.Series):
        hmm = hmm_prob_bull.reindex(p_bma.index, method="ffill").fillna(0.0)
    else:
        hmm = pd.Series(
            np.asarray(hmm_prob_bull, dtype=float),
            index=p_bma.index,
        ).fillna(0.0)

    bear_mask = hmm < threshold
    n_blocked = int(bear_mask.sum())
    pct = n_blocked / max(len(bear_mask), 1) * 100

    p_gated = p_bma.copy()
    p_gated[bear_mask] = 0.0

    log.info("hmm.gate", n_blocked=n_blocked, pct=round(pct, 1),
             threshold=threshold,
             mean_before=round(float(p_bma.mean()), 4),
             mean_after=round(float(p_gated.mean()), 4))

    if pct > 60:
        log.warning("hmm.too_strict", pct=round(pct, 1),
                     msg="HMM gate blocking >60% ÃƒÂ¢Ã¢â€šÂ¬Ã¢â‚¬Â check HMM calibration")
    elif pct < 15:
        log.warning("hmm.too_loose", pct=round(pct, 1),
                     msg="HMM gate blocking <15% ÃƒÂ¢Ã¢â€šÂ¬Ã¢â‚¬Â bear detection may be weak")
    return p_gated


