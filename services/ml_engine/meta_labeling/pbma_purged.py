        p_bma.iloc[test_idx] = p_fold

        log.info("pbma.fold_done", fold=fold_idx + 1,
                 n_train=len(train_idx), n_test=len(test_idx),
                 p_fold_mean=round(float(p_fold.mean()), 4))

