class PurgedKFold:
    """
    K-Fold com purge temporal e embargo.

    Para cada fold:
      - test_idx = bloco contÃƒÆ’Ã‚Â­guo [fold_start, fold_end)
      - purge_zone = [fold_start - embargo, fold_end + embargo)
      - train_idx = tudo FORA da purge_zone

    ReferÃƒÆ’Ã‚Âªncia: LÃƒÆ’Ã‚Â³pez de Prado, Advances in Financial Machine Learning,
    Cap. 7: Cross-Validation in Finance.

    Args:
        n_splits:    NÃƒÆ’Ã‚Âºmero de folds. Default 5.
        embargo_pct: FraÃƒÆ’Ã‚Â§ÃƒÆ’Ã‚Â£o do dataset como zona de embargo. Default 0.01.
                     embargo = max(1, int(N * embargo_pct))
    """

    def __init__(self, n_splits: int = 5, embargo_pct: float = 0.01):
        if n_splits < 2:
            raise ValueError(f"n_splits must be >= 2, got {n_splits}")
        self.n_splits = n_splits
        self.embargo_pct = embargo_pct

    def split(self, X, y=None, groups=None):
        """
        Gera (train_indices, test_indices) com purging + embargo.

        Yields:
            tuple[np.ndarray, np.ndarray]: (train_idx_purged, test_idx)
        """
        n = len(X) if hasattr(X, '__len__') else X.shape[0]
        embargo = max(1, int(n * self.embargo_pct))
        indices = np.arange(n)
        fold_size = n // self.n_splits

        for i in range(self.n_splits):
            test_start = i * fold_size
            test_end = (i + 1) * fold_size if i < self.n_splits - 1 else n
            test_idx = indices[test_start:test_end]

            # Zona de purge: [test_start - embargo, test_end + embargo)
            purge_start = max(0, test_start - embargo)
            purge_end = min(n, test_end + embargo)

            # Train = tudo fora da purge zone
            train_idx = np.concatenate([
                indices[:purge_start],
                indices[purge_end:]
            ])

            if len(train_idx) < 2:
                continue

            yield train_idx, test_idx

    def get_n_splits(self, X=None, y=None, groups=None) -> int:
        return self.n_splits

    def __repr__(self) -> str:
        return (f"PurgedKFold(n_splits={self.n_splits}, "
                f"embargo_pct={self.embargo_pct})")


