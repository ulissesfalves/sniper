def _safe_class_prior(y: np.ndarray, w: np.ndarray | None = None) -> float:
    """Prior robusto para fallback quando o fold degenera."""
    y_arr = np.asarray(y, dtype=float)
    if y_arr.size == 0:
        return 0.5

    if w is None:
        prior = float(np.nanmean(y_arr))
    else:
        w_arr = np.asarray(w, dtype=float)
        if w_arr.size != y_arr.size or np.sum(w_arr) <= 0:
            prior = float(np.nanmean(y_arr))
        else:
            prior = float(np.average(y_arr, weights=w_arr))

    if np.isnan(prior):
        prior = 0.5
    return float(np.clip(prior, 0.001, 0.999))


