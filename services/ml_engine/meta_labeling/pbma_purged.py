def load_vi_cluster_map() -> dict[str, list[str]] | None:
    """Carrega cluster_map.json da Fase 2, aceitando formatos legado e artefato completo."""
    for path_str in VI_CLUSTER_PATHS:
        p = Path(path_str)
        if not p.exists():
            continue
        try:
            with open(p, encoding="utf-8") as f:
                raw = json.load(f)

            cluster_map: dict[str, list[str]] = {}

            if isinstance(raw, dict):
                if isinstance(raw.get("cluster_map"), dict):
                    # artefato completo da Fase 2
                    for cid, feats in raw["cluster_map"].items():
                        if isinstance(feats, list):
                            cluster_map[str(cid)] = [str(x) for x in feats]
                elif isinstance(raw.get("feature_to_cluster"), dict):
                    # fallback: reconstrói cluster_map a partir do mapping feature -> cluster_id
                    for feat, cid in raw["feature_to_cluster"].items():
                        cluster_map.setdefault(str(cid), []).append(str(feat))
                else:
                    # formato puro cluster_id -> [features]
                    for cid, feats in raw.items():
                        if isinstance(feats, list):
                            cluster_map[str(cid)] = [str(x) for x in feats]

            cluster_map = {k: v for k, v in cluster_map.items() if isinstance(v, list) and len(v) > 0}
            if cluster_map:
                log.info("vi.loaded", path=str(p), n_clusters=len(cluster_map))
                return cluster_map
        except Exception as e:
            log.warning("vi.load_err", path=str(p), error=str(e))
    log.warning("vi.not_found", msg="Sem VI filtering - todas features.")
    return None


def select_features_by_vi(
    feature_df:    pd.DataFrame,
    target_series: pd.Series,
    cluster_map:   dict[str, list[str]],
) -> list[str]:
    """
    Seleciona UMA feature por cluster VI ÃƒÂ¢Ã¢â€šÂ¬Ã¢â‚¬Â a de maior MI com target.

    Para cada cluster com >1 feature redundante:
      1. MI(feature, target) para cada membro
      2. MantÃƒÆ’Ã‚Â©m a de maior MI, descarta o resto

    Clusters de 1 feature: mantÃƒÆ’Ã‚Â©m diretamente.
    """
    available = set(feature_df.columns)
    selected = []
    y = target_series.values.astype(int)

    for cid, feats in sorted(cluster_map.items(), key=lambda x: str(x[0])):
        present = [f for f in feats if f in available]
        if not present:
            continue
        if len(present) == 1:
            selected.append(present[0])
            continue

        best_feat, best_mi = present[0], -1.0
        for feat in present:
            x = feature_df[feat].values.astype(float)
            mask = ~(np.isnan(x) | np.isinf(x))
            xc, yc = x[mask], y[mask]
            if len(xc) < 50:
                continue
            try:
                n_u = len(np.unique(xc))
                if n_u <= 10:
                    xd = xc.astype(int)
                else:
                    kbd = KBinsDiscretizer(n_bins=min(10, n_u),
                                           encode="ordinal",
                                           strategy="quantile")
                    xd = kbd.fit_transform(xc.reshape(-1, 1)).ravel().astype(int)
                mi = mutual_info_score(xd, yc)
            except Exception:
                mi = 0.0
            if mi > best_mi:
                best_mi, best_feat = mi, feat

        selected.append(best_feat)
        discarded = [f for f in present if f != best_feat]
        if discarded:
            log.info("vi.pick", cluster=cid, kept=best_feat,
                     mi=round(best_mi, 4), dropped=discarded)

    n_in = sum(len(v) for v in cluster_map.values() if isinstance(v, list))
    log.info("vi.done", n_in=n_in, n_out=len(selected), features=selected)
    return selected


